type Use struct {
	DBName string
}

func (u *Use) Format(buf *TrackedBuffer) {
	buf.Fprintf("USE %s", u.DBName)
}

type CTE struct {
	TableName   *TableName
	ColumnExprs ColumnExprs
	Query       SelectStatement
}

func (c *CTE) Format(buf *TrackedBuffer) {
	buf.Fprintf("%v ", c.TableName)
	if len(c.ColumnExprs) > 0 {
		buf.Fprintf("(%v) ", c.ColumnExprs)
	}
	buf.Fprintf("as (%v)", c.Query)
}

type CTEs []*CTE

func (c CTEs) Format(buf *TrackedBuffer) {
	for i, cte := range c {
		buf.Fprintf("%v", cte)
		if i != len(c)-1 {
			buf.Fprintf(", ")
		}
	}
}

type With struct {
	CTEs      CTEs
	Recursive bool
}

func (w *With) Format(buf *TrackedBuffer) {
	buf.Fprintf("with ")
	if w.Recursive {
		buf.Fprintf("recursive ")
	}
	buf.Fprintf("%v", w.CTEs)
}

