// Select represents a SELECT statement.
type Select struct {
	With         *With
	Comments     Comments
	QueryGlobals *QueryGlobals
	SelectExprs  SelectExprs
	From         TableExprs
	Where        *Where
	GroupBy      GroupBy
	Having       *Where
	OrderBy      OrderBy
	Limit        *Limit
	Lock         string
}

// Select.QueryGlobals
type QueryGlobals struct {
	Distinct     bool
	StraightJoin bool
}

// Select.Distinct
const (
	AST_DISTINCT = "distinct "
)

// Select.Lock
const (
	AST_FOR_UPDATE = " for update"
	AST_SHARE_MODE = " lock in share mode"
)

func (node *Select) Format(buf *TrackedBuffer) {
	if node.With != nil {
		buf.Fprintf("%v ", node.With)
	}
	var distinct, straightJoin, from string
	if node.QueryGlobals != nil {
		if node.QueryGlobals.Distinct {
			distinct = "distinct "
		}
		if node.QueryGlobals.StraightJoin {
			straightJoin = "straight join "
		}
	}
	if node.From != nil {
		from = " from "
	}

	buf.Fprintf("select %v%s%s%v%s%v%v%v%v%v%v%s",
		node.Comments, distinct, straightJoin,
		node.SelectExprs, from, node.From, node.Where,
		node.GroupBy, node.Having, node.OrderBy,
		node.Limit, node.Lock)
}

