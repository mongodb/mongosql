// FuncExpr represents a function call.
type FuncExpr struct {
	Name      string
	Distinct  bool
	Exprs     SelectExprs
	OrderBy   OrderBy
	Separator option.String
}

func (node *FuncExpr) Format(buf *TrackedBuffer) {
	var distinct, separator string
	if node.Distinct {
		distinct = "distinct "
	}

	if node.Separator.IsSome() {
		separator = ` separator "` + node.Separator.Unwrap() + `"`
	}
	buf.Fprintf(
		"%s(%s%v%v%s)", node.Name,
		distinct, node.Exprs, node.OrderBy, separator,
	)
}

// CaseExpr represents a CASE expression.
type CaseExpr struct {
	Expr  Expr
	Whens Whens
	Else  Expr
}

func (node *CaseExpr) Format(buf *TrackedBuffer) {
	buf.Fprintf("case ")
	if node.Expr != nil {
		buf.Fprintf("%v ", node.Expr)
	}
	buf.Fprintf("%v ", node.Whens)
	if node.Else != nil {
		buf.Fprintf("else %v ", node.Else)
	}
	buf.Fprintf("end")
}

