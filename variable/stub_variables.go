func getDummyValue(v interface{}) func(*Container) interface{} {
	return func(c *Container) interface{} {
		return v
	}
}

func setDummyValue(name string) func(*Container, interface{}) error {
	return func(c *Container, v interface{}) error {
		log.Warnf(log.Dev, `the variable "%s" is unsupported. setting it will have no effect.`,
			name)
		return nil
	}
}

func setDummyValueErr(name string) func(*Container, interface{}) error {
	return func(c *Container, v interface{}) error {
		return mysqlerrors.Defaultf(mysqlerrors.ErVariableIsReadonly, "system", name)
	}
}
